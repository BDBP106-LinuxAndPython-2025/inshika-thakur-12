"""Q10.)Write a short note on seeking within files.
Ans.)Seeking in files means moving the file pointer to a certain spot within the file.
This is helpful when you want to read or write data at a specific place instead of going
through the file from the start. Seeking lets you reach any position inside the file,
which is useful for big files.You can move the pointer based on the start of the file,
the current spot, or the end of the file. This way, you can easily work with specific
sections without reading everything before it."""